import numpy as np

def sigmoid(x):
    return 1 / (1 + np.exp(-np.clip(x, -500, 500)))

def output_gate(h_prev: np.ndarray, x_t: np.ndarray, C_t: np.ndarray,
                W_o: np.ndarray, b_o: np.ndarray) -> tuple:
    # step1 = concate
    concate = np.concatenate((h_prev,x_t), axis =1)

    # step2 = output gate
    o_t = sigmoid((concate @ W_o.T) +b_o)

    # step 3 = hiden state
    h_t = o_t  * np.tanh(C_t)

    return o_t, h_t

